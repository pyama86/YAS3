			msgText = "„Åì„ÅÆ„Çπ„É¨„ÉÉ„Éâ„ÅØ„Ç§„É≥„Ç∑„Éá„É≥„Éà„Å´Á¥ê„Å•„Åë„Çâ„Çå„Å¶„ÅÑ„Åæ„Åõ„Çì"
